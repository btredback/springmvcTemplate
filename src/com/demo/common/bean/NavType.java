package com.demo.common.bean;

public class NavType
{
    private int typeID;

    private String typeName;

    private String typeDesc;

    private String typeUrl;

    private String typeHelp;

    private String typeDefaultCls;

    private int groupID;

    public int getTypeID()
    {
        return this.typeID;
    }

    public void setTypeID(int typeID)
    {
        this.typeID = typeID;
    }

    public String getTypeName()
    {
        return this.typeName;
    }

    public void setTypeName(String typeName)
    {
        this.typeName = typeName;
    }

    public String getTypeDesc()
    {
        return this.typeDesc;
    }

    public void setTypeDesc(String typeDesc)
    {
        this.typeDesc = typeDesc;
    }

    public String getTypeUrl()
    {
        return this.typeUrl;
    }

    public void setTypeUrl(String typeUrl)
    {
        this.typeUrl = typeUrl;
    }

    public String getTypeHelp()
    {
        return this.typeHelp;
    }

    public void setTypeHelp(String typeHelp)
    {
        this.typeHelp = typeHelp;
    }

    public String getTypeDefaultCls()
    {
        return this.typeDefaultCls;
    }

    public void setTypeDefaultCls(String typeDefaultCls)
    {
        this.typeDefaultCls = typeDefaultCls;
    }

    public int getGroupID()
    {
        return this.groupID;
    }

    public void setGroupID(int groupID)
    {
        this.groupID = groupID;
    }
}