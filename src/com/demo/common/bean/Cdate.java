package com.demo.common.bean;

public class Cdate
{
    private int year;

    private int month;

    private int weekDay;

    private int day;

    private int last;

    private int prop;

    private int exsit;

    public int getExsit()
    {
        return this.exsit;
    }

    public void setExsit(int exsit)
    {
        this.exsit = exsit;
    }

    public int getProp()
    {
        return this.prop;
    }

    public void setProp(int prop)
    {
        this.prop = prop;
    }

    public int getLast()
    {
        return this.last;
    }

    public int getYear()
    {
        return this.year;
    }

    public void setYear(int year)
    {
        this.year = year;
    }

    public void setLast(int last)
    {
        this.last = last;
    }

    public int getMonth()
    {
        return this.month;
    }

    public void setMonth(int month)
    {
        this.month = month;
    }

    public int getWeekDay()
    {
        return this.weekDay;
    }

    public void setWeekDay(int weekDay)
    {
        this.weekDay = weekDay;
    }

    public int getDay()
    {
        return this.day;
    }

    public void setDay(int day)
    {
        this.day = day;
    }
}