package com.demo.common.bean;

public class ActionType
{
    int actionTypeID;

    String actionTypeName;

    String actionTypeValue;

    String actionTypeParam;

    public int getActionTypeID()
    {
        return this.actionTypeID;
    }

    public void setActionTypeID(int actionTypeID)
    {
        this.actionTypeID = actionTypeID;
    }

    public String getActionTypeName()
    {
        return this.actionTypeName;
    }

    public void setActionTypeName(String actionTypeName)
    {
        this.actionTypeName = actionTypeName;
    }

    public String getActionTypeValue()
    {
        return this.actionTypeValue;
    }

    public void setActionTypeValue(String actionTypeValue)
    {
        this.actionTypeValue = actionTypeValue;
    }

    public String getActionTypeParam()
    {
        return this.actionTypeParam;
    }

    public void setActionTypeParam(String actionTypeParam)
    {
        this.actionTypeParam = actionTypeParam;
    }
}